    public void atualizarTabela() {
        
        String titulos[] = new String[4];
        titulos[0] = "Nome";
        titulos[1] = "E-mail";
        titulos[2] = "Senha";
        titulos[3] = "Permiss√£o";
        
        List<Usuario> usuarios = controle.consultarTodosUsuarios();
        this.listUsuarios = usuarios;

        String dados[][] = new String[usuarios.size()][4];
        for (int i = 0; i < usuarios.size(); i++) {
            dados[i][0] = usuarios.get(i).getNome();
            dados[i][1] = usuarios.get(i).getEmail();
            dados[i][2] = usuarios.get(i).getSenha();
            dados[i][3] = usuarios.get(i).getPermissao();
        }

        DefaultTableModel modeloTabela = new DefaultTableModel(dados, titulos);
        jTableUsuarios.setModel(modeloTabela);
    }

