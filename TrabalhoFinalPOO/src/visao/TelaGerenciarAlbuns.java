    public void atualizarTabela() {
        
        String titulos[] = new String[2];
        titulos[0] = "Nome";
        titulos[1] = "Artista";
        
        List<Albun> albuns = controle.consultarAlbuns();
        this.listAlbuns = albuns;

        String dados[][] = new String[albuns.size()][2];
        for (int i = 0; i < albuns.size(); i++) {
            dados[i][0] = albuns.get(i).getNome();
            dados[i][1] = albuns.get(i).getArtista().getNome();
        }

        DefaultTableModel modeloTabela = new DefaultTableModel(dados, titulos);
        jTableAlbuns.setModel(modeloTabela);
    }

