    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (albun.getIdAlbun() == 0) {
            controle.cadastrarAlbun(jTextFieldNome.getText(), (Artista) jComboBoxArtistas.getSelectedItem());
            jTextFieldNome.setText("");
            this.atualizarTabela();
        } else {
            controle.atualizarAlbun(albun.getIdAlbun(), jTextFieldNome.getText(), (Artista) jComboBoxArtistas.getSelectedItem());
            this.albun = new Albun();
            jTextFieldNome.setText("");
            this.atualizarTabela();
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
       int i = jTableAlbuns.getSelectedRow();
       this.albun = listAlbuns.get(i);
       jTextFieldNome.setText(albun.getNome());
       
       for(int index=0; index < this.artistas.size(); index++){
           if(artistas.get(index).getIdArtista() == albun.getArtista().getIdArtista()){
               jComboBoxArtistas.setSelectedIndex(index);
           }
       }
    }//GEN-LAST:event_jButtonEditarActionPerformed

